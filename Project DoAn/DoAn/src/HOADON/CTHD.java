    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        StringBuilder sb1 = new StringBuilder();
        if(jMaHD.getText().equals("") ){
            sb1.append("!!Mã hóa đơn không được để trống!!");
            jMaHD.setBackground(Color.red);
        }
        else{
            jMaHD.setBackground(Color.white);
        }
        StringBuilder sb2 = new StringBuilder();
        if(jMaSach.getText().equals("")){
            sb2.append("!!Mã sách không được để trống!!");
            jMaSach.setBackground(Color.red);
        }
        else{
            jMaSach.setBackground(Color.white);
        }
        if(sb1.length() > 0 || sb2.length() > 0){
            JOptionPane.showMessageDialog(this, sb1 + "\n" + sb2);
            return;
        }
        try{
            int result = JOptionPane.showConfirmDialog(null,"Bạn có muốn xóa chi tiết hóa đơn?", "Xóa", JOptionPane.YES_NO_OPTION);
            if(result == JOptionPane.YES_OPTION){
                CTHDmodify.DeleteCTHD(jMaHD.getText(), jMaSach.getText());
                JOptionPane.showMessageDialog(this,"Đã xóa chi tiết hóa đơn thành công");
            }
            else{
                JOptionPane.showMessageDialog(this,"Chi tiết hóa đơn chưa được xóa");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this,"Error : "+e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

